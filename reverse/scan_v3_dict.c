static const int n_anchors = sizeof(anchors) / sizeof(anchors[0]);

static int koi8r_cyr(uint8_t v) { return v >= 0xc0 && v != 0xff; }

static void load_keys(const char *path) {
    FILE *f = fopen(path, "r");
    char line[256]; if(!fgets(line, sizeof(line), f)){};
    while (fgets(line, sizeof(line), f)) {
        char *tab = strchr(line, '\t');
        if (tab) keys[n_keys++] = strtoull(tab+1, NULL, 0);
    }
    fclose(f);
    int u = 0;
    for (int i = 0; i < n_keys; i++) {
        int dup = 0;
        for (int j = 0; j < u; j++) if (keys[j] == keys[i]) { dup = 1; break; }
        if (!dup) keys[u++] = keys[i];
    }
    n_keys = u;
}

int main(int argc, char **argv) {
    FILE *f = fopen(argv[1], "rb");
    fseek(f, 0, SEEK_END); long sz = ftell(f); fseek(f, 0, SEEK_SET);
    uint8_t *data = malloc(sz); if(!fread(data, 1, sz, f)){}; fclose(f);
    load_keys(argv[2]);
    fprintf(stderr, "%d unique keys, %d anchors\n", n_keys, n_anchors);

    long ro_start = 0x837000;
    long ro_end   = 0xadb000;
    int hits = 0;

    printf("offset\tkey\trun\thex_pt\thex_ct\n");

    for (int ki = 0; ki < n_keys; ki++) {
        uint64_t k64 = keys[ki];
        uint8_t kb[8];
        for (int j = 0; j < 8; j++) kb[j] = (k64 >> (j*8)) & 0xff;

        for (long off = ro_start; off < ro_end - 64; off++) {
            /* Decode 8 bytes (one cipher counter block) */
            uint8_t pt[64];
            uint8_t prev = 0;
            int run = 8;
            for (int i = 0; i < 8; i++) {
                uint8_t c = data[off + i];
                uint8_t k = kb[i & 7];
                uint8_t tmp = c ^ k;          /* counter is 0 for i<8 */
                uint8_t v = (i == 0) ? tmp : (uint8_t)(tmp + prev);
                pt[i] = v;
                prev = v;
                if (v == 0) { run = i; break; }
            }
            /* Anchor match */
            int matched = 0;
            for (int a = 0; a < n_anchors; a++) {
                int al = anchors[a].len;
                if (al > run) continue;
                if (memcmp(pt, anchors[a].bytes, al) == 0) {
                    matched = 1;
                    break;
                }
            }
            if (!matched) continue;

            /* Extend up to 64 bytes */
            int ext = run;
            for (int i = run; i < 64; i++) {
                uint8_t c = data[off + i];
                uint8_t k = kb[i & 7];
                uint8_t ctr = (i >> 3) & 0xff;
                uint8_t tmp = c ^ ((k + ctr) & 0xff);
                uint8_t v = (uint8_t)(tmp + pt[i-1]);
                if (v == 0) { ext = i; break; }
                if (!(koi8r_cyr(v) || (v >= 0x20 && v < 0x7f))) { ext = i; break; }
                pt[i] = v;
                ext = i + 1;
            }

            printf("0x%08lx\t0x%016lx\t%d\t", off, k64, ext);
            for (int i = 0; i < ext; i++) printf("%02x", pt[i]);
            printf("\t");
            for (int i = 0; i < ext; i++) printf("%02x", data[off + i]);
            printf("\n");
            hits++;
        }
        if (ki % 200 == 0) fprintf(stderr, "  key %d/%d hits=%d\n", ki, n_keys, hits);
    }
    fprintf(stderr, "Total hits: %d\n", hits);
    return 0;
}
